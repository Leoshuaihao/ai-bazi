def get_gongwei_info(pillar: str) -> dict:
    """获取指定宫位的完整取象信息。

    Args:
        pillar: 宫位名（"年柱"、"月柱"、"日柱"、"时柱"）

    Returns:
        dict: 包含 domain, age_range, six_kin, event_types, social_layer
              若宫位名无效，返回空 dict
    """
    return GONGWEI_RULES.get(pillar, {})


def get_age_gongwei(age: int) -> str:
    """根据年龄获取当前应属的宫位。

    Args:
        age: 岁数 (1-99+)

    Returns:
        str: 宫位名（"年柱"、"月柱"、"日柱"、"时柱"）
    """
    for gw_name, rules in GONGWEI_RULES.items():
        lo, hi = rules["age_range"]
        if lo <= age <= hi:
            return gw_name
    # 超出年龄段：>=99 归时柱，<1 归年柱
    return "时柱" if age > 48 else "年柱"


def map_event_to_gongwei(event_type: str) -> str:
    """根据事件类型映射到对应宫位。

    Args:
        event_type: 事件类型（如"婚姻"、"学历"、"晚年运"）

    Returns:
        str: 对应的宫位名，若未找到映射则返回 "日柱" 作为默认
    """
    return EVENT_TO_GONGWEI.get(event_type, "日柱")


def gongwei_six_kin_cross_validate(
    gongwei: str,
    six_kin_type: str,
) -> bool:
    """宫位与六亲交叉验证。

    检查某类六亲在指定宫位是否有有效的映射关系。

    理论依据：
    《子平真诠·论六亲》"以十神定其本，以宫位明其位"
    - 年柱配祖父母/父母
    - 月柱配父母/兄弟
    - 日柱配自己/配偶
    - 时柱配子女

    Args:
        gongwei: 宫位名（"年柱"、"月柱"、"日柱"、"时柱"）
        six_kin_type: 六亲类型（如"祖父母"、"父母"、"兄弟"、"配偶"、"子女"等）

    Returns:
        bool: 该宫位是否匹配该六亲类型
    """
    rules = GONGWEI_RULES.get(gongwei)
    if rules is None:
        return False
    return six_kin_type in rules["six_kin"]
